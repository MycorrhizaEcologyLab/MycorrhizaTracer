	#[pprint.pprint(MetaDict[Sample_ID]["Metadata"]["SalvageGroup"]) for Sample_ID in MetaDict]  # Print metadata for each sample
	#len(set(MetaDict[Sample_ID]["Metadata"]["SalvageGroup"] for Sample_ID in MetaDict))
