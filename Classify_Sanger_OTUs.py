	#input("create fastq done, hold for sec")
	MetaDict = OTU.make_consensus_fastq(MetaDict, args)
	#pprint.pprint(MetaDict["A01"]["ITS"]["Seqs"])
	#pprint.pprint(MetaDict["A04"]["ITS"]["Seqs"])
	#input("make consensus done, hold for sec")
	#for Sample_ID in MetaDict:
	#	if MetaDict[Sample_ID]["ITS"]["Seqs"]["Cons"]["nSeqs"] == 1:
	#		print(f"Sample ID: {Sample_ID}")
	#		pprint.pprint(MetaDict[Sample_ID]["ITS"]["Seqs"])

	
	#classify sequences (clusters?) with blastn
